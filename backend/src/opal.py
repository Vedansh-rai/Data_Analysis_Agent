    def export_tableau(self, file_path: str) -> Dict[str, str]:
        """
        Generates a Tableau Data Source file for the dataset.
        """
        if not self.antigravity.load_data(file_path):
             return {"error": "Could not access dataset."}

        from tableau_connector import TableauConnector
        connector = TableauConnector()
        
        # Ensure output dir exists
        export_dir = os.path.join(self.output_dir, "exports")
        os.makedirs(export_dir, exist_ok=True)
        
        dtypes = {k: str(v) for k, v in self.antigravity.df.dtypes.items()}
        tds_path = connector.create_tableau_package(file_path, dtypes, export_dir)
        
        return {"path": tds_path, "filename": os.path.basename(tds_path)}

    def run_pipeline(self, file_path: str, user_query: str = "", x_axis: str = None, y_axis: str = None) -> Dict[str, Any]:
        """Runs the pipeline and returns the result."""
        print(f"💎 Opal: Starting analysis on {file_path} with query: '{user_query}'")
        
        # 1. Ingestion & Analysis
        if not self.antigravity.load_data(file_path):
            return {"error": "Pipeline failed at data loading."}

        summary = self.antigravity.analyze()
        print("✅ Analysis Complete.")

        # 2. Insight Generation
        insights = self.generate_dummy_insights(summary, user_query)
        
        # Add Trend Insight if y_axis is selected
        if y_axis:
            trend_insight = self.antigravity.calculate_trend(y_axis)
            if trend_insight:
                insights.append(f"🔮 **Predictive Insight**: {trend_insight}")

        # 3. Chart Generation
        generated_charts = []
        
        # If user specified axes, prioritize that chart
        if x_axis and y_axis:
            print(f"🎨 Generating Custom Chart: {x_axis} vs {y_axis}")
            # Determine best chart type
            is_y_numeric = y_axis in summary.numeric_stats
            
            if is_y_numeric:
                chart_spec = self.chart_engine.create_bar_chart(
                    self.antigravity.df.head(50), # Cap for performance
                    x_col=x_axis,
                    y_col=y_axis,
                    title=f"Custom Analysis: {y_axis} by {x_axis}"
                )
                chart_name = f"custom_chart_{y_axis}_by_{x_axis}.json"
                chart_path = os.path.join(self.output_dir, "charts", chart_name)
                self.chart_engine.save_chart_json(chart_spec, chart_path)
                self.chart_engine.save_chart_html(chart_spec, chart_path.replace('.json', '.html'))
                
                generated_charts.append({
                    "title": "Custom Analysis",
                    "path": chart_name,
                    "description": f"User selected visualization of **{y_axis}** by **{x_axis}**.",
                    "spec": chart_spec
                })

        # Fallback/Additional numeric charts (existing logic)
        numeric_cols = list(summary.numeric_stats.keys())
        if len(numeric_cols) >= 1 and not (x_axis and y_axis):
             # Only generate default if no custom chart, or add as secondary?
             # Let's add default logic as simplified "Overview" if no custom chart
             pass 
             
        # Always generate the default scatter if we have enough numeric cols and no specific override that conflicts
        if len(numeric_cols) >= 2 and (not generated_charts or len(generated_charts) < 2):
             target_col = numeric_cols[0]
             y_col = numeric_cols[1]
             # Check if we already plotted this
             if not (x_axis == target_col and y_axis == y_col):
                scatter_spec = self.chart_engine.create_scatter_plot(
                    self.antigravity.df.sample(min(100, len(self.antigravity.df))),
                    x_col=target_col,
                    y_col=y_col,
                    title=f"Correlation Overview: {target_col} vs {y_col}"
                )
                scatter_name = f"scatter_{target_col}_vs_{y_col}.json"
                scatter_path = os.path.join(self.output_dir, "charts", scatter_name)
                self.chart_engine.save_chart_json(scatter_spec, scatter_path)
                self.chart_engine.save_chart_html(scatter_spec, scatter_path.replace('.json', '.html'))

                generated_charts.append({
                     "title": "Correlation Overview",
                     "path": scatter_name,
                     "description": f"Exploring relationship between {target_col} and {y_col}.",
                     "spec": scatter_spec
                })

        # 4. Report Generation
        result = self.generate_report(summary, insights, generated_charts)
        print("✅ Opal: Pipeline finished successfully.")
        return result
