from antigravity_core import Antigravity, DatasetSummary
from chart_engine import ChartEngine

class Opal:
    """
    The Brain / Orchestrator.
    Manages the workflow: Ingestion -> Analysis -> Insights -> Visualization -> Reporting.
    """
    def __init__(self):
        self.antigravity = Antigravity()
        self.chart_engine = ChartEngine()
        self.output_dir = "output"

    def generate_dummy_insights(self, summary: DatasetSummary, user_query: str = "") -> List[str]:
        """
        Simulates AI-generated insights based on summary stats.
        In a real production env, this would call GPT-4o with the summary json.
        """
        insights = []
        insights.append(f"Analyzed {summary.total_rows} rows across {len(summary.columns)} columns.")
        
        # Acknowledge User Query
        if user_query:
            insights.append(f"💡 **Focusing on User Query**: '{user_query}'")
            if "sales" in user_query.lower() and "Sales" in summary.numeric_stats:
                insights.append("🔍 Deep dive: Sales data shows significant variance.")
            elif "satisfaction" in user_query.lower() and "Customer_Satisfaction" in summary.numeric_stats:
                insights.append("😊 Sentiment Analysis: Customer satisfaction is stable.")

        # Simple rule-based insight
        if any(v > 0 for v in summary.missing_values.values()):
            insights.append("⚠️ Data Quality Alert: Significant missing values detected in key columns.")
        
        # Numeric insights
        for col, stats in summary.numeric_stats.items():
            mean_val = stats.get('mean', 0)
            max_val = stats.get('max', 0)
            insights.append(f"📈 **{col}**: Average value is {mean_val:.2f}, peaking at {max_val}.")
            
        return insights

    def generate_report(self, summary: DatasetSummary, insights: List[str], charts: List[Dict]):
        """
        Compiles the findings into a Markdown report.
        """
        timestamp = datetime.datetime.now().strftime("%Y-%m-%d %H:%M")
        
        report_content = f"""# 📊 Automated Data Analysis Report
**Generated by Opal** at {timestamp}

## 1. Executive Summary
{insights[0]}

## 2. Key Insights
"""
        for i, insight in enumerate(insights[1:], 1):
            report_content += f"- {insight}\n"

        report_content += "\n## 3. Visual Analysis\n"
        
        for chart in charts:
            report_content += f"### {chart['title']}\n"
            report_content += f"{chart['description']}\n\n"
            # Embed HTML link or interactive component hint
            # Since markdown can't embed interactive JS easily without iframe, we link it or show fallback
            report_content += f"**[🔎 View Interactive Chart](charts/{chart['path'].replace('.json', '.html')})**\n\n"
            report_content += f"*(Chart spec available at `charts/{chart['path']}`)*\n\n"

        report_content += "\n## 4. Dataset Overview\n"
        report_content += f"- **Rows**: {summary.total_rows}\n"
        report_content += f"- **Columns**: {', '.join(summary.columns)}\n"
        
        if summary.issues:
            report_content += "\n### ⚠️ Data Issues Detected\n"
            for issue in summary.issues:
                report_content += f"- **{issue.column}**: {issue.description}\n"

        output_path = os.path.join(self.output_dir, "reports", "Analysis_Report.md")
        with open(output_path, 'w') as f:
            f.write(report_content)
        
        print(f"📄 Report generated at: {output_path}")
        return {
            "report_path": output_path,
            "report_content": report_content,
            "charts": charts,
            "summary": summary.dict()
        }

